package Entity;

public class Valutazione {
	private String Utente;
	private String TitoloBrano;
	private Integer Punteggio;
	private Integer NumeroVolte;

	public Valutazione(String Utente, String TitoloBrano, Integer Punteggio, Integer NumeroVolte) {

		this.Utente = Utente;
		this.TitoloBrano = TitoloBrano;
		this.Punteggio = Punteggio;
		this.NumeroVolte = NumeroVolte;

	}
}
